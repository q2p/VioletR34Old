package q2p.violetr34.addons.resources;

class ResourceType {
	final String storageName;
	final String name;
	final String beg;
	final String end;
	
	ResourceType(final String storageName, final String name, final String beg, final String end) {
		this.storageName = storageName;
		this.name = name;
		this.beg = beg;
		this.end = end;
	}
}